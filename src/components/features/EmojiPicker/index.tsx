export function EmojiPicker({ isOpen, onClose, onEmojiSelect, buttonRef }: EmojiPickerProps) {
  const [activeCategory, setActiveCategory] = useState('Smileys & People');
  const [selectedEmoji, setSelectedEmoji] = useState<string | null>(null);
  const pickerRef = useRef<HTMLDivElement>(null);

  // Fechar quando clicar fora
  useEffect(() => {
    const handleClickOutside = (event: MouseEvent) => {
      if (
        pickerRef.current && 
        !pickerRef.current.contains(event.target as Node) &&
        buttonRef.current &&
        !buttonRef.current.contains(event.target as Node)
      ) {
        onClose();
      }
    };

    if (isOpen) {
      document.addEventListener('mousedown', handleClickOutside);
    }

    return () => {
      document.removeEventListener('mousedown', handleClickOutside);
    };
  }, [isOpen, onClose, buttonRef]);

  // Fechar com ESC
  useEffect(() => {
    const handleKeyDown = (event: KeyboardEvent) => {
      if (event.key === 'Escape') {
        onClose();
      }
    };

    if (isOpen) {
      document.addEventListener('keydown', handleKeyDown);
    }

    return () => {
      document.removeEventListener('keydown', handleKeyDown);
    };
  }, [isOpen, onClose]);

  if (!isOpen) return null;

  return (
    <div ref={pickerRef} className={styles.emojiPicker}>
      <div className={styles.header}>
        <div className={styles.categories}>
          {Object.keys(emojiCategories).map((category) => (
            <button
              key={category}
              className={`${styles.categoryButton} ${
                activeCategory === category ? styles.active : ''
              }`}
              onClick={() => {
                setActiveCategory(category);
                setSelectedEmoji(null); // Limpar seleÃ§Ã£o ao trocar categoria
              }}
            >
              {category.split(' ')[0]}
            </button>
          ))}
        </div>
        <button 
          className={styles.closeButton}
          onClick={onClose}
          title="Fechar seletor de emojis"
        >
          âœ•
        </button>
      </div>
      
      <div className={styles.emojiGrid}>
        {emojiCategories[activeCategory as keyof typeof emojiCategories].map((emoji, index) => (
          <button
            key={`${emoji}-${index}`}
            className={`${styles.emojiButton} ${
              selectedEmoji === emoji ? styles.selected : ''
            }`}
            onClick={() => {
              onEmojiSelect(emoji);
              setSelectedEmoji(emoji);
              // Limpar seleÃ§Ã£o visual apÃ³s um breve delay
              setTimeout(() => setSelectedEmoji(null), 200);
            }}
            title={emoji}
          >
            {emoji}
          </button>
        ))}
      </div>
    </div>
  );
}